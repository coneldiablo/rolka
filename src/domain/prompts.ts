export function renderCharacterCard(character: PromptCharacter) {
  const parts = [
    `Имя: ${character.name}`,
    `Возраст: ${character.age}`,
    `Описание: ${character.description}`,
    character.appearance ? `Внешность: ${character.appearance}` : null,
    character.personality ? `Характер: ${character.personality}` : null,
    character.speechStyle ? `Стиль речи: ${character.speechStyle}` : null,
    character.setting ? `Сеттинг: ${character.setting}` : null,
    character.boundaries ? `Границы/запреты: ${character.boundaries}` : null,
    character.starterScene ? `Стартовая сцена: ${character.starterScene}` : null
  ];

  return parts.filter(Boolean).join("\n");
}

export function buildPrompt(input: BuildPromptInput): PromptMessage[] {
  const systemBlocks = [
    BASE_SAFETY_PROMPT,
    ANTI_AI_STYLE_PROMPT,
    ROLEPLAY_QUALITY_PROMPT,
    FORBIDDEN_PHRASES_PROMPT,
    `Режим: ${input.mode}\n${MODE_PROMPTS[input.mode]}`,
    `Персонажи:\n${input.characters.map(renderCharacterCard).join("\n\n")}`,
    input.lorebook ? `Лорбук:\n${input.lorebook}` : null,
    input.memorySummary ? `Краткая память:\n${input.memorySummary}` : null,
    input.importedContext ? `Импортированный контекст:\n${input.importedContext}` : null
  ].filter(Boolean);

  return [
    {
      role: "system",
      content: systemBlocks.join("\n\n---\n\n")
    },
    ...input.recentMessages
  ];
}
